  END AS office_name_norm
FROM tmp_info_center_user_import;

-- 补齐信息中心下缺失机构（仅按名称补齐）
INSERT INTO sys_office (
  id, officeCode, officeName, officeType, grade,
  parentId, parentIds, sort, status, useable, delFlag,
  createdAt, updatedAt
)
SELECT
  REPLACE(UUID(), '-', '') AS id,
  CONCAT('OFF-AUTO-', LPAD(CONV(CRC32(t.office_name_norm), 10, 16), 8, '0')) AS officeCode,
  t.office_name_norm,
  'DEPARTMENT',
  '3',
  p.id,
  CONCAT(COALESCE(p.parentIds, '0,'), p.id, ','),
  99,
  'ENABLED',
  '1',
  0,
  NOW(),
  NOW()
FROM (
  SELECT DISTINCT office_name_norm
  FROM tmp_info_center_user_norm
  WHERE office_name_norm IS NOT NULL AND office_name_norm <> ''
) t
JOIN sys_office p
  ON p.officeName COLLATE utf8mb4_unicode_ci = '信息中心' COLLATE utf8mb4_unicode_ci AND p.delFlag = 0
LEFT JOIN sys_office o
  ON o.officeName COLLATE utf8mb4_unicode_ci = t.office_name_norm COLLATE utf8mb4_unicode_ci
 AND o.parentId = p.id AND o.delFlag = 0
WHERE o.id IS NULL;

-- 用户 upsert（部门外检字段：officeId）
INSERT INTO sys_user (
  id, username, displayName, passwordHash, officeId, mobile, email, status, createdAt, updatedAt
)
SELECT
  REPLACE(UUID(), '-', '') AS id,
  t.username,
  t.display_name,
  SHA2(COALESCE(t.init_password, '123456'), 256) AS passwordHash,
  o.id AS officeId,
  t.mobile,
  NULL AS email,
  'ENABLED' AS status,
  NOW(),
  NOW()
FROM tmp_info_center_user_norm t
LEFT JOIN sys_office o
  ON o.officeName COLLATE utf8mb4_unicode_ci = t.office_name_norm COLLATE utf8mb4_unicode_ci
 AND o.delFlag = 0
WHERE t.username IS NOT NULL
  AND t.username <> ''
  AND t.display_name IS NOT NULL
  AND t.display_name <> ''
ON DUPLICATE KEY UPDATE
  displayName = VALUES(displayName),
  officeId = VALUES(officeId),
  mobile = VALUES(mobile),
  status = 'ENABLED',
  updatedAt = NOW();

